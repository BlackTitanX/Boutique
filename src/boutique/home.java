    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int fila = model.getRowCount();
        boolean answer = false;
        for(int i=0; i<fila; i++){
            String acum = "";
            if(String.valueOf(jTable1.getValueAt(i, 0)).length() != 0){
                for(int j=0; j<String.valueOf(jTable1.getValueAt(i, 5)).length(); j++){
                    if(acum.toLowerCase().equals(jTextField6.getText().toLowerCase())){
                        deleteTable(i);
                        jTable1.setSelectionBackground(Color.red);
                        jTable1.getSelectionModel().setSelectionInterval (0,0);
                        answer = true;
                        break;
                    }else{
                        acum += String.valueOf(jTable1.getValueAt(i, 5)).charAt(j);
                        if(String.valueOf(jTable1.getValueAt(i, 5)).charAt(j) == ' '){
                            acum = "";
                        }
                    }
                }
            }else{
                break;
            }
            if(answer){
                break;
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

