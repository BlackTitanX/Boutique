    private void deleteTable(int limit){
        int columna = model.getColumnCount();
        int fila = model.getRowCount();
        int cont=0;
        for(int i=0; i<fila; i++){
            if(i<limit){
                cont++;
            }else if(i>limit){
                for(int j=0; j<columna; j++){
                    jTable1.setValueAt("", i, j);
                }
            }
        }
        for(int i=0; i<cont; i++){
            model.removeRow(0);
        }
    }
    
